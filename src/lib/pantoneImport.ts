export async function importPantoneColors(): Promise<boolean> {
  try {
    const colorsRef = collection(db, 'pantoneColors');
    
    // Получаем существующие коды цветов
    const existingCodes = new Set();
    const snapshot = await getDocs(colorsRef);
    snapshot.docs.forEach(doc => {
      const data = doc.data();
      if (data.code) {
        existingCodes.add(data.code);
      }
    });

    // Фильтруем только новые цвета
    const newColors = pantoneColors.filter(color => !existingCodes.has(color.code));

    if (newColors.length === 0) {
      console.log('Нет новых цветов для импорта');
      return true;
    }

    // Импортируем только новые цвета пакетами
    const batchSize = 500;
    for (let i = 0; i < newColors.length; i += batchSize) {
      const batch = writeBatch(db);
      const chunk = newColors.slice(i, Math.min(i + batchSize, newColors.length));
      
      for (const color of chunk) {
        const docRef = doc(colorsRef);
        batch.set(docRef, {
          ...color,
          createdAt: new Date().toISOString()
        });
      }
      
      await batch.commit();
      console.log(`Импортировано новых цветов: ${i + 1} - ${Math.min(i + batchSize, newColors.length)}`);
    }

    console.log(`Успешно импортировано ${newColors.length} новых цветов`);
    return true;
  } catch (error) {
    console.error('Ошибка импорта цветов:', error);
    return false;
  }
}